--器材仪器表
CREATE TABLE APPARATUS (APPA_ID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 6000, INCREMENT BY 1 ),
	APPA_NAME VARCHAR(512),
	PATH VARCHAR(128),
	DEPT VARCHAR(128),
	MANAGER VARCHAR(128),
	STATUS VARCHAR(10),
	PRICE INTEGER DEFAULT 0, 
	ROLE INTEGER ,
	TYPE INTEGER 
	IMPORT_TIME TIMESTAMP,
	PRIMARY KEY (APPA_ID)
);

--新闻信息表
CREATE TABLE NEWS (NEWS_ID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 10000, INCREMENT BY 1 ),
	TITLE VARCHAR(512),
	PUBLISH_TIME TIMESTAMP,
	CONTENT CLOB,
	PRIMARY KEY (NEWS_ID)
);

--文档表
CREATE TABLE DOCUMENTS(
DOC_ID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 10000, INCREMENT BY 1 ),
TITLE VARCHAR(124),
USER_ID INTEGER,
PATH VARCHAR(124),
UPLOAD_TIME TIMESTAMP,
PRIMARY KEY (DOC_ID)
)

--留言表
CREATE TABLE MESSAGE(
	MSG_ID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 10000, INCREMENT BY 1 ),
	NAME VARCHAR(24),
	GENDER VARCHAR(10),
	EMAIL VARCHAR(36),
	CONTENT VARCHAR(124),
	UPLOAD_TIME TIMESTAMP,
	PRIMARY KEY (MSG_ID)
)
