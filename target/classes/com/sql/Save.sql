--建表语句
CREATE TABLE USERS (
ID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1000, INCREMENT BY 1 ),
NAME VARCHAR(24) NOT NULL,
PASSWORD VARCHAR(64),
USER_TYPE VARCHAR(12),
TELEPHONE VARCHAR(11),
ADDRESS VARCHAR(512),
NATIONALITY VARCHAR(24),
PHOTO BLOB,
LITERATURE_NUM INT DEFAULT 0,
DELETE_NUM INT DEFAULT 0,
UPLOAD_TIME TIMESTAMP,
PRIMARY KEY (ID)
);

--文件表
CREATE TABLE FILES(FILE_ID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 10000, INCREMENT BY 1 ),
	TITLE VARCHAR(512) NOT NULL,
	FILE_TYPE VARCHAR(12),
	CONTENT CLOB,
	ATTACH_NUM VARCHAR(512),
	UPLOAD_TIME TIMESTAMP,
	DELETE_TIME TIMESTAMP,
	USER_ID INTEGER,
	PRIMARY KEY (FILE_ID)
);

--附件表
CREATE TABLE ATTACHMENT(ATTACH_ID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 9000, INCREMENT BY 1 ),
	FILE_ID INTEGER NOT NULL,
	USER_ID INTEGER,
	ATTACH_NAME VARCHAR(512),
	PATH VARCHAR(128),
	DELETE_FLAG INTEGER DEFAULT 0, 
	UPLOAD_TIME TIMESTAMP,
	DELETE_TIME TIMESTAMP,
	PRIMARY KEY (ATTACH_ID)
);
